import turtle

turtle.title("Awesome turtle")
turtle.setup(425, 425, 0, 0)
turtle.pencolor("#0000FF")
turtle.goto(0, 0)

turtle.fillcolor("#ff0000")
turtle.begin_fill()
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.end_fill()


turtle.exitonclick()
